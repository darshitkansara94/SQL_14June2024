Database :
	Database is collection of table, store procedure, function and views.
	Space and special char is not allowed in Database name.
	Database name should be : EmployeeData.
	First char can not be number or special char.
	In syntax "create database" is not case sensitive.
	Duplicate database name is not allowed.

	Syntax :	
		create database databasename

	Example :
		create database EmployeeData

	To delete / remove database from server we are using 'Drop' keyword.

	Syntax :
		drop database databasename

	Example :
		drop database sampledata